class OperatorDemo7 {
   public static void main(String args[]) {
   int num1=1;   // = is assignment operator
   int num2=2;
   System.out.println(num1==num2); // == is comparison operator  false
   System.out.println(num1>num2);  // false
   System.out.println(num1!=num2); // true
   System.out.println(num1<num2);  // true
   System.out.println(num1>=num2); // false
   System.out.println(num1<=num2); //  true 
   }
 } 